// –ú–∞—Ä—à—Ä—É—Ç: GET –¥–ª—è –∑–∞–≥—Ä—É–∑–∫–∏ –ø–æ—Å–ª–µ–¥–Ω–∏—Ö –¥–∞–Ω–Ω—ã—Ö –∏–∑ –ë–î
app.get('/data', (req, res) => {
  const query = 'SELECT data FROM excel_data ORDER BY uploaded_at DESC LIMIT 1';
  db.query(query, (err, results) => {
    if (err) {
      console.error('–û—à–∏–±–∫–∞ —á—Ç–µ–Ω–∏—è –∏–∑ –ë–î:', err);
      return res.status(500).json({ error: '–û—à–∏–±–∫–∞ —á—Ç–µ–Ω–∏—è: ' + err.message });
    }
    if (results.rows.length === 0) {
      console.log('–ë–î –ø—É—Å—Ç–∞ ‚Äî –≤–æ–∑–≤—Ä–∞—â–∞–µ–º []');
      return res.json({ success: false, data: [] });
    }

    const rawData = results.rows[0].data;
    console.log('–°—ã—Ä—ã–µ –¥–∞–Ω–Ω—ã–µ –∏–∑ –ë–î (–ø–µ—Ä–≤—ã–µ 100 —Å–∏–º–≤–æ–ª–æ–≤):', typeof rawData === 'string' ? rawData.substring(0, 100) : JSON.stringify(rawData).substring(0, 100)); // –î–µ–±–∞–≥-–ª–æ–≥

    try {
      let parsedData;
      if (typeof rawData === 'object' && rawData !== null) {
        // –ï—Å–ª–∏ pg —É–∂–µ —Ä–∞—Å–ø–∞—Ä—Å–∏–ª (JSONB-—Ç–∏–ø) ‚Äî –∏—Å–ø–æ–ª—å–∑—É–µ–º –∫–∞–∫ –µ—Å—Ç—å
        parsedData = rawData;
      } else if (typeof rawData === 'string') {
        // –ï—Å–ª–∏ —Å—Ç—Ä–æ–∫–∞ ‚Äî –ø–∞—Ä—Å–∏–º
        parsedData = JSON.parse(rawData);
      } else {
        throw new Error('–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π —Ç–∏–ø –¥–∞–Ω–Ω—ã—Ö –≤ –ë–î: ' + typeof rawData);
      }

      // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ parsedData ‚Äî –º–∞—Å—Å–∏–≤
      if (!Array.isArray(parsedData)) {
        throw new Error('–î–∞–Ω–Ω—ã–µ –Ω–µ –º–∞—Å—Å–∏–≤: ' + typeof parsedData);
      }

      console.log('–ü–∞—Ä—Å–∏–Ω–≥ —É—Å–ø–µ—à–µ–Ω! –ö–æ–ª-–≤–æ —Å—Ç—Ä–æ–∫:', parsedData.length);
      res.json({ success: true, data: parsedData });
    } catch (parseErr) {
      console.error('–û—à–∏–±–∫–∞ –ø–∞—Ä—Å–∏–Ω–≥–∞ JSON –∏–∑ –ë–î:', parseErr);
      res.status(500).json({ error: '–û—à–∏–±–∫–∞ –ø–∞—Ä—Å–∏–Ω–≥–∞ –¥–∞–Ω–Ω—ã—Ö: ' + parseErr.message + '. –û—á–∏—Å—Ç–∏ —Ç–∞–±–ª–∏—Ü—É: DELETE FROM excel_data;' });
    }
  });
});

